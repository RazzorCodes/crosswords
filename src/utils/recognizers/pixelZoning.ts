export function recognizePixelZoning(strokes: Point[][]): { char: string; score: number } {
  const grid = getPixelZones(strokes);
  let bestScore = -1;
  let bestChar = '';

  for (const [char, template] of Object.entries(TEMPLATES)) {
    let match = 0;
    let totalExpected = 0;
    
    // Tanimoto coefficient style matching
    for (let i = 0; i < grid.length; i++) {
      if (template[i] === 1) totalExpected++;
      if (grid[i] === 1 && template[i] === 1) match++;
      // Penalize drawing where there shouldn't be
      if (grid[i] === 1 && template[i] === 0) match -= 0.5;
    }
    
    // Score is rough overlap percentage
    let score = match / totalExpected;
    if (score > bestScore) {
      bestScore = score;
      bestChar = char;
    }
  }

  return { char: bestChar, score: bestScore };
}
