export type Point = { x: number; y: number };

const ZONES = 8;

// Helper to normalize strokes into an 8x8 grid (0 to 1 intensity)
export function getPixelZones(strokes: Point[][]): number[] {
  let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
  
  for (const stroke of strokes) {
    for (const p of stroke) {
      if (p.x < minX) minX = p.x;
      if (p.x > maxX) maxX = p.x;
      if (p.y < minY) minY = p.y;
      if (p.y > maxY) maxY = p.y;
    }
  }

  const width = maxX - minX;
  const height = maxY - minY;
  
  if (width === 0 && height === 0) return new Array(ZONES * ZONES).fill(0);

  const maxDim = Math.max(width, height);
  // Pad slightly
  const padding = maxDim * 0.1;
  const scale = ZONES / (maxDim + 2 * padding);
  const offsetX = -minX + padding + (maxDim - width) / 2;
  const offsetY = -minY + padding + (maxDim - height) / 2;

  // We will draw it onto a small virtual 8x8 grid
  const grid = new Array(ZONES * ZONES).fill(0);
  
  // Basic rasterization (Bresenham's line algorithm on an 8x8 grid)
  const drawLine = (x0: number, y0: number, x1: number, y1: number) => {
    let dx = Math.abs(x1 - x0);
    let dy = Math.abs(y1 - y0);
    let sx = (x0 < x1) ? 1 : -1;
    let sy = (y0 < y1) ? 1 : -1;
    let err = dx - dy;

    while (true) {
      if (x0 >= 0 && x0 < ZONES && y0 >= 0 && y0 < ZONES) {
        grid[y0 * ZONES + x0] = 1;
      }
      if (x0 === x1 && y0 === y1) break;
      let e2 = 2 * err;
      if (e2 > -dy) { err -= dy; x0 += sx; }
      if (e2 < dx) { err += dx; y0 += sy; }
    }
  };

  for (const stroke of strokes) {
    if (stroke.length === 0) continue;
    let prevX = Math.floor((stroke[0].x + offsetX) * scale);
    let prevY = Math.floor((stroke[0].y + offsetY) * scale);
    
    // Just marking points for a very basic rendering
    if (prevX >= 0 && prevX < ZONES && prevY >= 0 && prevY < ZONES) {
        grid[prevY * ZONES + prevX] = 1;
    }

    for (let i = 1; i < stroke.length; i++) {
      const currX = Math.floor((stroke[i].x + offsetX) * scale);
      const currY = Math.floor((stroke[i].y + offsetY) * scale);
      drawLine(prevX, prevY, currX, currY);
      prevX = currX;
      prevY = currY;
    }
  }

  return grid;
}

